import { useEffect, useRef, useState } from 'react';

/**
 * Drop-target key used by the "All Items" row; maps to folderId = null.
 * Folder rows use their folder id as the key.
 */
export const ROOT_DROP_TARGET_KEY = '__root__';

const DROP_TARGET_ATTRIBUTE = 'data-folder-drop-target';
const DRAG_THRESHOLD_PX = 6;

export interface FolderDragState {
  captureIds: string[];
  x: number;
  y: number;
  /** Drop-target key under the cursor, or null when not over a target */
  targetKey: string | null;
}

interface UseDragToFolderProps {
  containerRef: React.RefObject<HTMLDivElement | null>;
  enabled: boolean;
  /** Ref so drag start sees the current selection without re-binding listeners */
  selectedIdsRef: React.RefObject<Set<string>>;
  onDrop: (captureIds: string[], targetKey: string) => void;
}

function findDropTargetKey(x: number, y: number): string | null {
  return (
    document
      .elementFromPoint(x, y)
      ?.closest(`[${DROP_TARGET_ATTRIBUTE}]`)
      ?.getAttribute(DROP_TARGET_ATTRIBUTE) ?? null
  );
}

/**
 * Mouse-tracked drag of capture cards onto the folder rail.
 *
 * Implemented with plain mouse events rather than HTML5 drag-and-drop because
 * Tauri's native file drag-drop handling (needed for import) suppresses
 * in-page HTML5 drag events on Windows WebView2. Dragging a card that is part
 * of a multi-selection drags the whole selection.
 */
export function useDragToFolder({
  containerRef,
  enabled,
  selectedIdsRef,
  onDrop,
}: UseDragToFolderProps): FolderDragState | null {
  const [dragState, setDragState] = useState<FolderDragState | null>(null);
  const dragStateRef = useRef<FolderDragState | null>(null);
  const pendingRef = useRef<{ captureId: string; startX: number; startY: number } | null>(null);

  // Keep the latest onDrop without re-binding the window listeners
  const onDropRef = useRef(onDrop);
  onDropRef.current = onDrop;

  useEffect(() => {
    if (!enabled) {
      return;
    }

    const updateDragState = (state: FolderDragState | null) => {
      dragStateRef.current = state;
      setDragState(state);
    };

    const cancelDrag = () => {
      pendingRef.current = null;
      updateDragState(null);
    };

    // One-shot capture-phase listener that swallows the click generated by
    // releasing a drag, so the card underneath doesn't get selected/opened.
    const suppressNextClick = () => {
      window.addEventListener(
        'click',
        (event) => {
          event.stopPropagation();
          event.preventDefault();
        },
        { capture: true, once: true }
      );
    };

    const handleMouseDown = (event: MouseEvent) => {
      if (event.button !== 0) return;

      const target = event.target as HTMLElement;
      // Only start drags from cards inside this library's grid container.
      // The listener lives on the document because the grid container mounts
      // after loading and swaps between the static and virtualized stages.
      if (!containerRef.current?.contains(target)) return;
      // Buttons inside the card (favorite, tags, delete) keep their own behavior
      if (target.closest('button')) return;

      const captureId = target.closest('[data-capture-id]')?.getAttribute('data-capture-id');
      // Optimistic placeholders have no backend id to move yet
      if (!captureId || captureId.startsWith('temp_')) return;

      pendingRef.current = { captureId, startX: event.clientX, startY: event.clientY };
    };

    const handleMouseMove = (event: MouseEvent) => {
      const pending = pendingRef.current;
      if (!pending) return;

      if (!dragStateRef.current) {
        const distance = Math.hypot(event.clientX - pending.startX, event.clientY - pending.startY);
        if (distance < DRAG_THRESHOLD_PX) return;

        const selected = selectedIdsRef.current;
        const captureIds =
          selected.has(pending.captureId) && selected.size > 1
            ? Array.from(selected)
            : [pending.captureId];
        updateDragState({ captureIds, x: event.clientX, y: event.clientY, targetKey: null });
        return;
      }

      event.preventDefault();
      updateDragState({
        ...dragStateRef.current,
        x: event.clientX,
        y: event.clientY,
        targetKey: findDropTargetKey(event.clientX, event.clientY),
      });
    };

    const handleMouseUp = (event: MouseEvent) => {
      const active = dragStateRef.current;
      if (active) {
        const targetKey = findDropTargetKey(event.clientX, event.clientY);
        if (targetKey !== null) {
          onDropRef.current(active.captureIds, targetKey);
        }
        suppressNextClick();
      }
      cancelDrag();
    };

    const handleKeyDown = (event: KeyboardEvent) => {
      if (event.key === 'Escape' && dragStateRef.current) {
        cancelDrag();
      }
    };

    // A native drag (e.g. of a card thumbnail <img>) would swallow all mouse
    // events and kill the custom drag, so block it while one is pending.
    const handleDragStart = (event: DragEvent) => {
      if (pendingRef.current || dragStateRef.current) {
        event.preventDefault();
      }
    };

    document.addEventListener('mousedown', handleMouseDown);
    document.addEventListener('dragstart', handleDragStart);
    window.addEventListener('mousemove', handleMouseMove);
    window.addEventListener('mouseup', handleMouseUp);
    window.addEventListener('keydown', handleKeyDown);

    return () => {
      document.removeEventListener('mousedown', handleMouseDown);
      document.removeEventListener('dragstart', handleDragStart);
      window.removeEventListener('mousemove', handleMouseMove);
      window.removeEventListener('mouseup', handleMouseUp);
      window.removeEventListener('keydown', handleKeyDown);
      cancelDrag();
    };
  }, [containerRef, enabled, selectedIdsRef]);

  // Grabbing cursor for the whole app while dragging
  const isDragging = dragState !== null;
  useEffect(() => {
    if (!isDragging) return;

    const previousCursor = document.body.style.cursor;
    document.body.style.cursor = 'grabbing';
    return () => {
      document.body.style.cursor = previousCursor;
    };
  }, [isDragging]);

  return dragState;
}
